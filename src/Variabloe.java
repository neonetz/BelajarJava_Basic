public class Variabloe {
    public static void main(String[] args) {

        //Penamaan variable dengan string
        String name;
        name = "Muhammad Adhyaksa Fadillah";

        //Tampilkan variable name ke layar
        System.out.println(name);

        //Deklrasi dan inisiasi variable bertipe data integer dengan nilai 17
        int age = 17;

        //Penamaan variable dengan string address
        String address = "North Korea";

        //Tampilkan variable age dan address ke layar
        System.out.println(age);
        System.out.println(address);

        //Mengganti variable name dari MAF ke Cid Kagenoh
        name = "Cid Kagenoh";

        //Tampilkan variable name ke layar setelah di ubah
        System.out.println(name);

        //Deklrasi dan inisiasi variable dengan menggunakan var
        var namadepan = "Muhammad";
        var namatengah = "Adhyaksa";
        var namaterakhir = "Fadillah";

        //Tampilkan variable ke layar
        System.out.print(namadepan);
        System.out.print(namatengah);
        System.out.print(namaterakhir);

        //Deklrasi dan inisiasi variable konstanta
        final String pemainbluearchive = "Tidak waras";

        //Tampilkan variabe konstanta ke layar
        System.out.println(pemainbluearchive);

    }
}
