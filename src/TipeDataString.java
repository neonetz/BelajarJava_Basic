public class TipeDataString {

    public static void main(String[] args) {

        //Deklarasi Tipe Data String
        String namapertama = "Sakayanagi";
        String namaterakhir = " Arisu";
        String waifu = " Is My Waifu";

        //Deklrasi Tipe Data String dengan di Gabungkan
        String gabungan = namapertama + namaterakhir + waifu + " ";


        //Output Ke Terminal
        System.out.print(namapertama);
        System.out.print(namaterakhir);
        System.out.println(waifu);

        //OutPut setelah di gabungkan
        System.out.print(gabungan);

    }
}
