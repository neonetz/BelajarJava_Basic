public class WhileLoop {
    public static void main(String[] args) {
    //Kondisi dengan menggunakan while loop tanpa init statement dan post statement
        //Deklrasi dan inisiasi variabel
        var counter = 1;
        //perulangan sederhana sama seperti for loop diperlukan jika hanya ingin mengcek kondisi saja
        while(counter <= 10){
            System.out.println("Perulangan ke - "+ counter);
            counter++;

        }

    }
}
