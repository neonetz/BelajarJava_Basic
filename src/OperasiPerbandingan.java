public class OperasiPerbandingan {
    public static void main(String[] args) {

        //Deklrasi dan inisiasi tipe data number
        int value1 = 100;
        int value2 = 100;


        //Bandingkan kemudian tampilkan ke layar
        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);

    }
}
