public class Komentar {
    public static void main(String[] args) {
        //Panggil method kemudian tampilkan ke layar
        System.out.println(sum(10,10));
    }

    /**
     * Method untuk menjumlahkan value 1 dan value 2
     * @param value1 angka pertama
     * @param value2 angka kedua
     * @return hasil penjumlahan dari value 1 dan value 2
     */
    static int sum(int value1, int value2){
        //Jumlah value 1 dan value 2
        return value1 + value2;

    }
}
