public class TipeChar {
    public static void main(String[] args) {

        //Konversi Tipe Data Character
        char a = 'A';
        char k = 'K';
        char s = 'S';

        //Ouput ke Terminal Tidak Menggunakan Line (ln)
        System.out.print(a);
        System.out.print(k);
        System.out.print(s);
        System.out.println(a);

        //Output ke Terminal Menggunakan Line
        System.out.println(a);
        System.out.println(k);
        System.out.println(s);
        System.out.println(a);

    }
}
